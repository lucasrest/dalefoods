class Restaurante{

    constructor(objeto){
        this.horarioAbertura = objeto.horarioAbertura;
        this.horarioFechamento = objeto.horarioFechamento;
        this.nome = objeto.nome;
        this.descricao = objeto.descricao;
        this.endereco = objeto.endereco;
        this.cep = objeto.cep;
        this.bairro = objeto.bairro;
        this.cidade = objeto.cidade;
        this.estado = objeto.estado;
        this.email = objeto.email;
    }
}